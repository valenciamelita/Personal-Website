$conn = new mysqli($servername, $username, $password, $dbname);

if ($conn->connect_error) {
    die("Connection failed: " . $conn->connect_error);
}

$table_name = "kirim";

// Check if the table exists
$table_check_sql = "SHOW TABLES LIKE '$table_name'";
$table_check_result = $conn->query($table_check_sql);

// If the table doesn't exist, create it
if ($table_check_result->num_rows == 0) {
    $create_table_sql = "CREATE TABLE $table_name (
        name VARCHAR(255) NOT NULL,
        email VARCHAR(255) NOT NULL,
        company VARCHAR(255),
        phone VARCHAR(255),
        message TEXT NOT NULL,
        created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
    )";

    if ($conn->query($create_table_sql) === FALSE) {
        echo "Error creating table: " . $conn->error . ";";
    } 
}

// Check if the form is submitted
if (isset($_POST['submitBtn'])) {
    $name = $_POST['name'];
    $email = $_POST['email'];
    $company = $_POST['company'];
    $phone = $_POST['phone'];
    $message = $_POST['message'];

    // Prepare SQL statement for insertion
    $insert_sql = "INSERT INTO $table_name (name, email, company, phone, message)
                   VALUES ('$name', '$email', '$company', '$phone', '$message')";

    // Execute the insertion query
    if ($conn->query($insert_sql) === TRUE) {
        echo "<script>
               confirm('Your message is saved.')
              </script>";
    } else {
        echo "Error: " . $insert_sql . "<br>" . $conn->error;
    }
}

$conn->close();
